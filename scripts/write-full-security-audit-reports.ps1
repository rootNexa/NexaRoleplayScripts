function Read-AuditJson {
    param([string]$Name)
    $path = Join-Path $dataDir "$Name.json"
    if (-not (Test-Path -LiteralPath $path)) {
        return [pscustomobject]@{ findings = @(); skipped = @() }
    }
    return Get-Content -LiteralPath $path -Raw | ConvertFrom-Json
}

function Get-RepoFiles {
    $files = & rg --files --hidden -g '!.git/**' -g '!**/node_modules/**' -g '!**/dist/**' -g '!**/build/**' -g '!**/.next/**' -g '!**/coverage/**' -g '!**/vendor/**' -g '!.agents/**' -g '!.codex/**'
    return @($files | Where-Object { $_ -and (Test-Path -LiteralPath (Join-Path $root $_)) })
}

function Escape-Cell {
    param($Value)
    return (Stringify $Value).Replace('|', '\|').Replace("`r", ' ').Replace("`n", ' ')
}

function Stringify {
    param($Value)
    if ($null -eq $Value) { return '' }
    return [string]$Value
}

function Finding-Table {
    param($Findings, [int]$Limit = 80)
    $rows = @('| Severity | File | Line | Rule | Finding | Evidence |', '| --- | --- | ---: | --- | --- | --- |')
    foreach ($finding in @($Findings | Select-Object -First $Limit)) {
        $rows += '| {0} | {1} | {2} | {3} | {4} | {5} |' -f (Escape-Cell $finding.Severity), (Escape-Cell $finding.Path), (Escape-Cell $finding.Line), (Escape-Cell $finding.Rule), (Escape-Cell $finding.Message), (Escape-Cell $finding.Evidence)
    }
    if (@($Findings).Count -gt $Limit) {
        $rows += ''
        $rows += "_Table truncated to $Limit rows. Full finding count: $(@($Findings).Count)._"
    }
    return ($rows -join "`n")
}

function Write-Report {
    param(
        [string]$Path,
        [string]$Title,
        [string]$Scope,
        $Findings,
        [string[]]$Extra = @()
    )
    $counts = @($Findings | Group-Object Severity | ForEach-Object { "$($_.Name)=$($_.Count)" }) -join ', '
    if ($counts -eq '') { $counts = 'none' }
    $content = @(
        "# $Title",
        '',
        'Generated by static repository audit tooling. This report does not prove runtime safety.',
        '',
        "## Scope",
        '',
        $Scope,
        '',
        "## Summary",
        '',
        "- Finding count: $(@($Findings).Count)",
        "- Severity counts: $counts",
        "- Runtime FXServer validation: not executed in this pass.",
        "- MariaDB migration execution: not executed in this pass."
    ) + $Extra + @(
        '',
        '## Findings',
        '',
        (Finding-Table $Findings)
    )
    Set-Content -LiteralPath (Join-Path $root $Path) -Value ($content -join "`n") -Encoding UTF8
}

