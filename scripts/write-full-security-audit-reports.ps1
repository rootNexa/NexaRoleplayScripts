$graph = @()
$resourceRows = @('| Resource | Dependencies | Exports | Network Events | NUI | DB Access | Findings |', '| --- | --- | --- | ---: | --- | --- | ---: |')
foreach ($manifest in $manifestFiles) {
    $manifestPath = Join-Path $root $manifest
    $resource = Split-Path -Leaf (Split-Path -Parent $manifest)
    $dir = Split-Path -Parent $manifest
    $content = Get-Content -LiteralPath $manifestPath -Raw
    $deps = New-Object System.Collections.Generic.List[string]
    foreach ($m in [regex]::Matches($content, 'dependency\s+[\x27\x22]([^\x27\x22]+)[\x27\x22]|dependencies\s*\{(?<block>[\s\S]*?)\}')) {
        if ($m.Groups[1].Success) { $deps.Add($m.Groups[1].Value) | Out-Null }
        if ($m.Groups['block'].Success) {
            foreach ($inner in [regex]::Matches($m.Groups['block'].Value, '[\x27\x22]([^\x27\x22]+)[\x27\x22]')) { $deps.Add($inner.Groups[1].Value) | Out-Null }
        }
    }
    $resourceFiles = @($files | Where-Object { $_.StartsWith($dir.Replace('\', '/') + '/') })
    $exports = @()
    $events = 0
    $db = $false
    foreach ($rf in $resourceFiles | Where-Object { $_ -like '*.lua' }) {
        $text = Get-Content -LiteralPath (Join-Path $root $rf) -Raw
        $events += ([regex]::Matches($text, 'RegisterNetEvent|RegisterNUICallback')).Count
        if ($text -match 'MySQL\.|oxmysql|Database\.|GetCoreObject\(\).*Database') { $db = $true }
        foreach ($m in [regex]::Matches($text, 'exports\s*\(\s*[\x27\x22]([^\x27\x22]+)[\x27\x22]')) { $exports += $m.Groups[1].Value }
    }
    $hasNui = $content -match 'ui_page'
    $findingCount = @($all.findings | Where-Object { $_.Path -like "$dir/*" -or $_.Path -like "$resource/*" }).Count
    $resourceRows += '| {0} | {1} | {2} | {3} | {4} | {5} | {6} |' -f (Escape-Cell $resource), (Escape-Cell (($deps | Select-Object -Unique) -join ', ')), (Escape-Cell (($exports | Select-Object -Unique) -join ', ')), $events, $hasNui, $db, $findingCount
    $graph += [pscustomobject]@{ resource = $resource; manifest = $manifest; dependencies = @($deps | Select-Object -Unique); nui = $hasNui; database_access = $db }
}
